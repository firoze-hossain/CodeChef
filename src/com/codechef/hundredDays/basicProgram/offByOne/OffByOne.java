package com.codechef.hundredDays.basicProgram.offByOne;

import java.util.Scanner;

public class OffByOne {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        System.out.println(a + b + "1");
    }
}
