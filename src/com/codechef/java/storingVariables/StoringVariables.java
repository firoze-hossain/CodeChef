package com.codechef.java.storingVariables;

public class StoringVariables {
    public static void main(String[] args) {
        int width = 20;
        int length = 30;
        System.out.println(length * width);
        System.out.println(2 * (length * width));
        width = 15;
        System.out.println(length * width);
        System.out.println(2 * (length * width));
    }
}
