package com.codechef.java.stringOperations;

public class StringOperations {
    public static void main(String[] args) {
        String var = "String";
        System.out.print(var.charAt(0));
        System.out.print(var.charAt(1));
        System.out.println(var.charAt(2));
        System.out.print(var.substring(0, 3));
    }
}
