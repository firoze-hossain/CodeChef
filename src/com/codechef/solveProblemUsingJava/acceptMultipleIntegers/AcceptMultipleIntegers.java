package com.codechef.solveProblemUsingJava.acceptMultipleIntegers;

import java.util.Scanner;

public class AcceptMultipleIntegers {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int A = sc.nextInt();
        int B = sc.nextInt();
        int C = sc.nextInt();
        int D = sc.nextInt();
        int E = sc.nextInt();
        System.out.print(A + " " + B + " " + C + " " + D + " " + E);
    }
}
