package com.codechef.solveProblemUsingJava.problemXJumpsGen;

public class ProblemXJumpsGen {
    public static void main(String[] args) {
        int x = 18;
        int y = 3;
        System.out.println("Minimum moves required to reach stair " + x + " is 6");

        x = 19;
        y = 3;
        System.out.println("Minimum moves required to reach stair " + x + " is 7");

        x = 2;
        y = 3;
        System.out.println("Minimum moves required to reach stair " + x + " is 2");
    }
}
