package com.codechef.solveProblemUsingJava.testCases3;

import java.util.Scanner;

public class TestCases3 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        for (int i = 0; i < t; i++) {
            int a = sc.nextInt();
            int b = sc.nextInt();
            String s = sc.next();
            System.out.println(a + " " + b + " " + s);
        }
    }
}
